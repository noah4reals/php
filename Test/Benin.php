            <div id="Part2">
                <article>
                    <h3 id="Localisation et frontières">Localisation et frontières</h3>
                    <p>Le Bénin partage 2 123 km de frontières terrestres avec quatre pays : le <a href="Burkina-Faso.html" target="_blank">Burkina Faso</a> (386 km), le <a href="Niger.html" target="_blank">Niger</a> (277 km), le <a href="Nigeria.html" target="_blank">Nigeria</a> (809 km) et le <a href="Togo.html" target="_blank">Togo</a> (651 km). La Cour internationale de justice des Nations unies a défini le 12 juillet 2005 la frontière actuelle entre le Bénin et le <a href="Niger.html" target="_blank">Niger</a>, après un différend au sujet des îles dans le lit des fleuves <a href="Niger.html" target="_blank">Niger</a> et Mékrou : neuf îles ont été attribuées au Bénin et seize, dont celle de Lété, au <a href="Niger.html" target="_blank">Niger</a>.</p>
                    <p>La ligne côtière, sur le golfe (ou baie) du Bénin – dans le grand golfe de Guinée – s'étend sur 121 km.</p>
                    <p>Le territoire, formé d'une étroite bande de terre orientée perpendiculairement à la côte, s'étend du nord au sud sur une longueur d'environ 672 km. Il atteint une largeur de 324 km en son point le plus large.</p>
                    </p>
                    <h3 id="Relief">Relief</h3>
                    <p>De forme étirée entre le fleuve Niger au nord et la plaine côtière dans le sud, le relief de l'ensemble du pays est peu accidenté.</p>
                    <p>Le nord du pays est principalement constitué de savanes et de montagnes semi-arides, telles que la chaîne de l'Atacora, qui se prolonge au <a href="Togo.html" target="_blank">Togo</a> et au Ghana d'une part et au <a href="Niger.html" target="_blank">Niger</a> d'autre part. Le point culminant du pays est le mont Sokbaro(ou Sagbarao) (800 mètres).</p>
                    <p>Le sud du pays est constitué d'une plaine côtière basse parsemée de marécages, lacs et lagunes, notamment la basse vallée de l'Ouémé, la lagune de Porto-Novo et le lac Nokoué, une vaste zone humide de 91 600 ha reconnue d'importance internationale par la convention de Ramsar.</p>
                    <p>La majeure partie de la population vit dans les plaines côtières méridionales, dont l'altitude ne dépasse nulle part 10 m. C'est là que les plus grandes villes du Bénin sont concentrées, notamment Porto-Novo, la capitale officielle, et Cotonou, la capitale économique et politique.</p>
