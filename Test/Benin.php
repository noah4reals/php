			<h2 id="Politique">Politique et Administration</h2>
            <div id="Part2">
                <article>
                    <p>Le 6 avril 2006, le nouveau président de la République du Bénin, 54 ans, est officiellement installé dans ses villas à Cotonou.</p>
                    <p>Le nouveau président qui prône une « République coopérative et solidaire », a énuméré les quatre priorités de son mandat que sont les ressources humaines, une gouvernance concertée, le développement de l'esprit d'entreprise, la construction de nouvelles infrastructures.</p>
                    <p>L'ancien président de la<b> ​Banque ouest-africaine de développement​</b> (BOAD) est élu président de la République à l'issue du deuxième tour de scrutin le 5 mars 2006, rassemblant 74,51 % des suffrages, contre 25,49 % pour M​<sup>e</sup>​ Adrien Houngbédji, qui a présenté ses félicitations au nouvel élu.</p>
                    <p>Candidat indépendant, Boni Yayi a su rallier les ténors de la politique béninoise que sont Albert Tévoédjrè, Émile Derlin Zinsou et une vingtaine de députés à l'Assemblée nationale, avant de bénéficier des consignes de vote de presque tous ses concurrents du premier tour, à l'issue duquel il totalisait un peu plus de 35 %, contre 24 % pour son poursuivant M​<sup>e</sup>​ Adrien Houngbédji.</p>
                    <p>Apparemment, les consignes de vote ont été suivies. Toutefois, certains observateurs estiment qu'avec ou sans consignes, le « candidat du changement » serait passé. Aux yeux des électeurs et plus particulièrement des jeunes et des milieux d'affaires, Boni Yayi (économiste) incarne l'espoir d'une reprise économique, l'amoindrissement du chômage, la lutte contre la corruption, la bonne gouvernance.</p>
                    <p>Le successeur de Mathieu Kérékou a promis un taux de croissance à deux chiffres (environ 5 % actuellement) et le positionnement du Bénin en tête des producteurs du coton ouest-africains à partir de la campagne agricole 2006-2007.</p>
                    <p>Quoique entouré de toute la classe politique, Boni Yayi se refuse à faire de la ​<i>politique politicienne.</i>. « Nous sommes venus pour produire de la richesse », dit-il, refusant de constituer un « gouvernement de remerciement ». Cependant, des sources bien informées indiquent qu'il a demandé aux partis politiques de lui proposer des cadres pour la formation du gouvernement.</p>
                    <p>Les élections législatives du 31 mars 2007 donnent la majorité à la Force Cauris pour un Bénin émergent (FCBE).</p>
                    <p>Le président Boni Yayi a été réélu pour un second mandat lors des élections présidentielles de mars 2011. Obtenant plus de 55 % des voix, contre 35 % pour son principal concurrent Adrien Houngbédji, Boni Yayi a été élu dès le premier tour. Il s'est engagé, dès sa prise de fonction, à ne pas modifier la constitution dans le but de briguer un troisième mandat et quitte donc ses fonctions en mars 2016, à l'issue des prochaines élections présidentielles. Lui succède Patrice Talon, candidat indépendant et ancien homme d'affaires.</p>
