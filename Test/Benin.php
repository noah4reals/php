		<section class="General">
			<article>
				<p>Le ​<b>Bénin​</b> en forme longue la <b>​république du Bénin​</b> , est un pays d'Afrique de l'Ouest, qui couvre une superficie de 114 764 km​<sup>2</sup>​ et s'étend sur 700 km, du fleuve Niger au nord à la côte atlantique au sud. Le Bénin comptait 10 741 458 habitants en 2016. Le pays fait partie de la CÉDÉAO et a comme voisins le <a href="Togo.html" target="_blank">Togo</a> à l'ouest, le <a href="Nigeria.html" target="_blank">Nigeria</a> à l'est, et au nord le <a href="Niger.html" target="_blank">Niger</a> et le <a href="Burkina-Faso.html" target="_blank">Burkina Faso</a>.</p>
			</article>
			<article> 
				<p>Le <a href="benin.html">Bénin</a> a accédé à l'indépendance complète le 1​<sup>er</sup>​ août 1960, sous la dénomination de <b><i>République du Dahomey</b></i>. Les pouvoirs furent transmis au président <i>​Hubert Maga</i> par le ministre d'État français ​<i>Louis Jacquinot</i>. En 1972, l'officier ​<i>Mathieu Kérékou</i> prend le pouvoir : il adopte en ​<i>1974</i>​ le ​<b>marxisme-léninisme​</b> comme idéologie officielle du gouvernement et, en <i>1975</i>, rebaptise le pays ​<b>République populaire du Bénin</b>​. À la fin des années 1980, de graves difficultés économiques conduisent à la fin du régime : le Bénin entame un processus de transition démocratique et, en 1990, adopte ​une nouvelle constitution​. Le nom de Bénin est conservé, le pays devenant simplement la République du Bénin. ​<i>Mathieu Kérékou</i>,​ battu aux élections, abandonne le pouvoir. Il y revient démocratiquement par les urnes en ​<i>1996​</i> et ne rétablit pas la dictature ; il gouverne le pays jusqu'en <i>​2006</i>.</p>
			</article>
			<article>
				<p>La capitale officielle est Porto-Novo (nommée ​<i>​Xogbonou</i>​ par les Goun et <i>​​Adjatchè</i>​ par les Yorubas), Cotonou étant la capitale économique. </p>
				<p>Le Bénin a comme langue officielle le français et comme monnaie le franc CFA. Le régime politique du Bénin est de type présidentiel et le président de la République entrant est <i><b>​Patrice Talon</b></i>, qui a succédé à ​<i><b>Boni Yayi</b></i> lors des élections du 20 mars 2016. La passation de pouvoir entre le président sortant Boni Yayi et son successeur, l'homme d'affaires Patrice Talon s'est tenue le 6 avril 2016 au palais de la ​<b>Marina à Cotonou</b>​. Le Bénin fait partie de plusieurs organisations internationales, dont l'<i>​Organisation internationale de la francophonie</i></p>
			</article>
			<article>
				<p>Depuis la fin de la République populaire du Bénin, le Bénin possède une image très forte de pays démocratique dans toute l'Afrique subsaharienne. Et selon les experts internationaux cette nation d'Afrique peut être considérée comme la plus stable de par ses institutions constitutionnelle et politique.</p>
			</article>
